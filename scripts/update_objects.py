def process_data(data, mapper, uuid_list, source):
    for hot_key, content in data.items():
        challenge = get_challenge_for_hotkey(hot_key)
        if not challenge:
            continue

        positions = content["positions"]
        for position in positions:
            mapper[position["position_uuid"]] = source
            uuid_list.append(position["position_uuid"])

    return mapper, uuid_list


def update_transactions():
    with TaskSessionLocal_() as db:
        mapper = {}
        uuid_list = []

        test_net_data = call_checkpoint_api()
        main_net_data = call_main_net()

        mapper, uuid_list = process_data(test_net_data, mapper, uuid_list, source="test")
        mapper, uuid_list = process_data(main_net_data, mapper, uuid_list, source="main")

        result = db.execute(select(Transaction.uuid).distinct())
        trade_uuids = [row.uuid for row in result.all()]

        transactions = db.query(Transaction).all()

        for obj in transactions:
            source = mapper.get(obj.uuid)
            if not source:
                continue

            obj.source = source
            obj.modified_by = obj.trader_id
            db.commit()
            db.refresh(obj)

        print(uuid_list)
